    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:

        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
            String produto = txtAdicionarProduto.getText();
            LocalDateTime dataDeInclusao = LocalDateTime.now();
            String data = dataDeInclusao.format(formatter);

            DefaultTableModel modeloTabela = (DefaultTableModel) table.getModel();
            Object[] rowData = {produto, data};

            modeloTabela.addRow(rowData);

            limpar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro.");
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        try {
            int linhaSelecionada = table.getSelectedRow();

            if (linhaSelecionada != -1) {
                DefaultTableModel tabela = (DefaultTableModel) table.getModel();
                tabela.removeRow(linhaSelecionada);
            } else {
                JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

